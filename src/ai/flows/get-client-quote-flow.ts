const ClientQuoteInputSchema = z.object({
  gender: z.enum(['male', 'female']).default('female'),
});
export type ClientQuoteInput = z.infer<typeof ClientQuoteInputSchema>;

const ClientQuoteOutputSchema = z.string();

export async function getClientQuote(input: ClientQuoteInput): Promise<string> {
  return clientQuoteFlow(input);
}

const clientQuoteFlow = ai.defineFlow(
  {
    name: 'clientQuoteFlow',
    inputSchema: ClientQuoteInputSchema,
    outputSchema: ClientQuoteOutputSchema,
  },
  async ({ gender }) => {
    const quotes = gender === 'male' ? maleQuotes : femaleQuotes;
    const randomIndex = Math.floor(Math.random() * quotes.length);
    return quotes[randomIndex];
  }
);
